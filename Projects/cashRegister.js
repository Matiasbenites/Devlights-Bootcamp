/* 
>Design a cash register drawer function checkCashRegister() that accepts purchase price as the first argument (price), payment as the second argument (cash), and cash-in-drawer (cid) as the third argument.

cid is a 2D array listing available currency.

The checkCashRegister() function should always return an object with a status key and a change key.

Return {status: "INSUFFICIENT_FUNDS", change: []} if cash-in-drawer is less than the change due, or if you cannot return the exact change.

Return {status: "CLOSED", change: [...]} with cash-in-drawer as the value for the key change if it is equal to the change due.

Otherwise, return {status: "OPEN", change: [...]}, with the change due in coins and bills, sorted in highest to lowest order, as the value of the change key.
Currency Unit	Amount
Penny	$0.01 (PENNY)
Nickel	$0.05 (NICKEL)
Dime	$0.1 (DIME)
Quarter	$0.25 (QUARTER)
Dollar	$1 (ONE)
Five Dollars	$5 (FIVE)
Ten Dollars	$10 (TEN)
Twenty Dollars	$20 (TWENTY)
One-hundred Dollars	$100 (ONE HUNDRED)

See below for an example of a cash-in-drawer array:

[
  ["PENNY", 1.01],
  ["NICKEL", 2.05],
  ["DIME", 3.1],
  ["QUARTER", 4.25],
  ["ONE", 90],
  ["FIVE", 55],
  ["TEN", 20],
  ["TWENTY", 60],
  ["ONE HUNDRED", 100]
]
function checkCashRegister(price, cash, cid) {
  let change;
  return change;
}

checkCashRegister(19.5, 20, [["PENNY", 1.01], ["NICKEL", 2.05], ["DIME", 3.1], ["QUARTER", 4.25], ["ONE", 90], ["FIVE", 55], ["TEN", 20], ["TWENTY", 60], ["ONE HUNDRED", 100]]);
*/
function checkCashRegister(price, cash, cid) {
  let t_Cid = 0;
  let status = { status: "", change: [] };
  let change = cash - price;

  let cidObj = {
    PENNY: 0.01,
    NICKEL: 0.05,
    DIME: 0.1,
    QUARTER: 0.25,
    ONE: 1,
    FIVE: 5,
    TEN: 10,
    TWENTY: 20,
    "ONE HUNDRED": 100,
  };

  for (let i = 0; i < cid.length; i++) {
    t_Cid = t_Cid + cid[i][1];
  }

  t_Cid = Math.round(t_Cid * 100) / 100;

  if (change === t_Cid) {
    status.status = "CLOSED";
    status.change = cid;
    return status;
  } else if (t_Cid < change) {
    status.status = "INSUFFICIENT_FUNDS";
    status.change = [];
  } else {
    for (let i = cid.length - 1; i >= 0; i--) {
      if (change >= cidObj[cid[i][0]] && change >= cid[i][1]) {
        status.change.push(cid[i]);
        change = change - cid[i][1];
        change = Math.round(change * 100) / 100;
      } else if (change >= cidObj[cid[i][0]] && change < cid[i][1]) {
        let amont = cidObj[cid[i][0]] * Math.floor(change / cidObj[cid[i][0]]);

        status.change.push([cid[i][0], amont]);
        change = change - amont;
        change = Math.round(change * 100) / 100;
      }
    }
  }

  if (0.01 <= change) {
    status.status = "INSUFFICIENT_FUNDS";
    status.change = [];
  } else {
    status.status = "OPEN";
  }
  return status;
}

